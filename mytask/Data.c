/****
	* @brief  UI元素参数的平滑过渡控制函数，核心作用是让某个 UI 参数（如位置、尺寸、透明度等）从当前值逐步逼近目标值
	* @param  int* a：指向当前值的指针
	* @param  int* a_trg：指向目标值的指针
	* @param  int b：步长（每次更新的增量 / 减量，控制动画速度）
	* @retval 无
*****/
void ui_run(int* a, int* a_trg, int b)
{
	if(*a < *a_trg){
		*a+=b;
		if(*a > *a_trg)
		   *a = *a_trg;
	}
	if(*a > *a_trg){
		*a-=b;
		if(*a < *a_trg)
		   *a = *a_trg;
	}
	if(*a == *a_trg){
		*a = *a_trg;
	}
}

